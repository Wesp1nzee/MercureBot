        for i in range(1, 11):
            tasks_string += f"{i} –ù–æ–º–µ—Ä. –ö–æ–ª-–≤–æ: {result_dict[i]}\n"

        tasks_string += f"\n–í—Å–µ–≥–æ —Ä–µ—à–µ–Ω–æ - {sum(result)}"
