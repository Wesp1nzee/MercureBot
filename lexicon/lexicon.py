async def generate_tasks_string(
        user_id: int | str, 
        object: str
        )-> str:
    """
    –ì–µ–Ω–µ—Ä–∏—Ä—É–µ—Ç —Å—Ç–∞—Ç–∏—Å—Ç–∏–∫—É –¥–ª—è –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è. 
    """
    tasks_string = ""
    result = list(map(lambda x: x-1, await db.get_task_users(user_id=user_id, object=object)))#–î–æ—Å—Ç–∞—ë–º –∏–∑ –ë–¥ –≤—Å–µ –∑–∞–¥–∞—á–∫–∏ user
    result_dict = {index: value for index, value in enumerate(result, start=1)}

